package ru.job4j;

public class Unicode {
    public static void main(String[] args) {
        char c1 = '\u0035';
        char c2 = '\u004D';
        char c3 = '\u006E';
        System.out.println(c1);
        System.out.println(c2);
        System.out.println(c3);
        System.out.println("\u0031\u0032\u0033");
        char a = 'A';
        char b = 'B';
        System.out.println(a + b);
    }
}
