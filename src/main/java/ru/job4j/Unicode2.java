package ru.job4j;

public class Unicode2 {
    public static void main(String[] args) {
        int a = 'A';
        int b = 'B';
        System.out.println("A: " + a);
        System.out.println("B: " + b);
        System.out.println("A + B: " + (a + b));
        char c = 'C';
        System.out.println(++c);
        char d = 'D';
        System.out.println(--d);
    }
}
